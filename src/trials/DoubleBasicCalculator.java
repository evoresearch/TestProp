package trials;

public class DoubleBasicCalculator {
    public int add(int a,int b){
        return a+b;
    }
    public int subtract(int a,int b){
        return a-b;
    }

    public double multiply(int a,int b){
        return a*b;
    }
    public double divide(int a,int b){
        return a/b;
    }
    public double divide(double a,double b){
        return a/b;
    }
}
